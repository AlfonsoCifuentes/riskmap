def display_summary(results):
    """Mostrar resumen de resultados"""
    print(f"\n{'='*60}")
    print("ğŸ“Š RESUMEN DE PRUEBAS DEL SISTEMA DE FALLBACK")
    print(f"{'='*60}")
    
    successful_tests = [r for r in results if r['success']]
    failed_tests = [r for r in results if not r['success']]
    
    print(f"âœ… Pruebas exitosas: {len(successful_tests)}/{len(results)}")
    print(f"âŒ Pruebas fallidas: {len(failed_tests)}")
    
    if successful_tests:
        print(f"\nğŸ¯ Proveedores utilizados:")
        providers = {}
        for result in successful_tests:
            provider = result.get('provider', 'unknown')
            if provider not in providers:
                providers[provider] = 0
            providers[provider] += 1
            
        for provider, count in providers.items():
            print(f"  - {provider}: {count} usos")
        
        avg_time = sum(r['duration'] for r in successful_tests) / len(successful_tests)
        print(f"\nâ±ï¸  Tiempo promedio: {avg_time:.2f}s")
    
    print(f"\nğŸ’¡ CONCLUSIONES:")
    
    ollama_used = any(r.get('provider', '').startswith('ollama') for r in successful_tests)
    groq_used = any(r.get('provider') == 'groq' for r in successful_tests)
    
    if ollama_used and not groq_used:
        print("  ğŸ¯ Sistema funcionando completamente con Ollama")
        print("  ğŸš€ DeepSeek manejando anÃ¡lisis complejos")
        print("  âš¡ Gemma optimizando resÃºmenes rÃ¡pidos")
